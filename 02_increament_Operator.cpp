#include <iostream>
using namespace std;

int main(){
    int a = 10, b = 20;
    cout<<"The increament after print is "<<++a<<endl;
    cout<<"The print after increament is "<<a++<<endl;
    cout<<"The Value of a becomes "<<a<<endl;
    cout<<"The decreament after print is "<<--b<<endl;
    cout<<"The print after decreament is "<<b--<<endl;
    cout<<"The Value of b becomes "<<b<<endl;
    return 0;
}
